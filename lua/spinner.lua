M.style = "dots"


function M.start()
    if running then
        return
    end
    running = true
    local chars = frames[M.style]
    local i = 1
    local timer = vim.loop.new_timer()
    timer:start(1000,
    120, function()
        if not running then
            timer:close()  -- Always close handles to avoid leaks.
        end
        i = (i + 1) % #chars
        state = chars[i+1]
    end)
end

function M.stop()
    running = false
end

